//Print numbers from 1 to 10
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

//Print numbers from 20 to 1
for (let i = 20; i >= 1; i--) {
  console.log(i);
}

//Odd numbers from 1 to 50
for (let i = 1; i <= 50; i += 2) {
  console.log(i);
}

// a while loop that prints Bangladesh 5 times
let count = 0;
while (count < 5) {
    console.log("Bangladesh");
    count++;
}

